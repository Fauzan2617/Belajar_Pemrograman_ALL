# Operasi Boolean
# NOT,OR,AND,XOR

# Perintah NOT membuat hasil menjadi kebalikan dari nilai yang diberikan
print("===NOT===")
a = True
c = not a
print("data c adalah", c)

# Perintah OR dimana salah satu benar maka hasilnya TRUE/benar
# Atau keduanya TRUE maka hasilnya benar/TRUE
print("===OR===")
a = True
b = False
c = a or b
print("data c adalah", c)

# Menjadi True misalkan keduanya TRUE/BENAR
print("===AND===")
a = True
b = False
c = a and b
print("data c adalah", c)

# TRUE misalkan salah satu TRUE
print("===XOR===")
a = True
b = False
c = a ^ b
print("data c adalah", c)
