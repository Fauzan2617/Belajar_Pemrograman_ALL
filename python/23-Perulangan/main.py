# perulangan (Loop)


#Penulisan Pengulangan
#For Kondisi:aksi

angka2_list= [0,1,2,3,4,5]
print(angka2_list)

for i in angka2_list:
    print(f"i sekarang adalah {i}")

print("akhir program")


# contoh dengan Range
angka2_range = range(1,5) # 1 adalah batas awal range bukan menjadi desimal

for i in angka2_range:
        print(f"i sekarang adalah {i}")

print("akhir program")


